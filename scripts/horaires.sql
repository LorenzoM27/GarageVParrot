CREATE TABLE garage_VParrot.horaires
(
    id int NOT NULL AUTO_INCREMENT PRIMARY KEY,
    lundi VARCHAR(35) NOT NULL,
    mardi VARCHAR(35) not null,
    mercredi VARCHAR(35) not null,
    jeudi VARCHAR(35) not null,
    vendredi VARCHAR(35) not null,
    samedi VARCHAR(35) not null,
    dimanche VARCHAR(35) not null
)